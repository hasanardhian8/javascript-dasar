function number(n) {
    return Math.sqrt(n)
}
console.log((number(9)));

function number1(a,b) {
    return Math.pow(a,b)
}
console.log((number1(2,3)));

function number2(c) {
    return Math.round(c)
}
console.log((number2(2.6)));

function number3(d) {
    return Math.floor(d)
}
console.log((number3(2.6)));

function number4(a,b,c,d,e) {
    return Math.max(a,b,c,d,e) //min = kecil
}
console.log((number4(1,2,3,4,5)));

